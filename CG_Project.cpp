/**
 * updates the OpenGL viewport when the window size changes.
 *
 * @param new_width the new window width.
 * @param new_hight the new window height.
 * @return nothing, it refreshes the viewport and projection.
 */
void reshapeFunc(GLint new_width, GLint new_hight)
{
    glViewport(0, 0, new_width, new_hight);
    win_width = new_width;
    win_hight = new_hight;
}

// ---------- display ----------

/**
 * chooses which scene to draw based on the current scene number.
 *
 * @return nothing, it draws the current frame on the screen.
 */
void display()
{
    glClear(GL_COLOR_BUFFER_BIT);

    glMatrixMode(GL_MODELVIEW);
    glLoadIdentity();

    // source: https://stackoverflow.com/questions/20677023/change-scenes-in-game-opengl.com
    if (scene == 1)
        Scene1();

    else if (scene == 2)
        Scene2();

    else if (scene == 3)
        Scene3();

    else if (scene == 4)
        Scene4();

    else if (scene == 5)
        Scene5();

    else if (scene == 6)
        Scene6();

    glFlush();
    glutSwapBuffers();
}

// ---------- main ----------

/**
 * starts the GLUT program and connects all callbacks.
 *
 * @param argc number of command line arguments.
 * @param argv command line argument values.
 * @return zero when the program ends normally.
 */
int main(int argc, char** argv)
{

    glutInit(&argc, argv); // initialize GLUT
    glutInitDisplayMode(GLUT_RGBA | GLUT_DOUBLE | GLUT_DEPTH); // use RGBA, double buffer, and depth buffer
    glutInitWindowSize(500, 500); // set the window's initial width and height
    glutInitWindowPosition(250, 50); // position the window's initial top-left corner
    glutCreateWindow("OpenGL_Project _ Be Kind"); // create a window with the given title

    init();

    glutIdleFunc(play);
    glutDisplayFunc(display);
    glutReshapeFunc(reshapeFunc);
    glutMouseFunc(mouse);
    glutKeyboardFunc(key);

    glutMainLoop(); // enter the main event loop

    return 0;
}

