/**
 * handles mouse clicks on the story choice buttons.
 *
 * @param button mouse button that was clicked.
 * @param state tells if the button was pressed or released.
 * @param x mouse x position in window coordinates.
 * @param y mouse y position in window coordinates.
 * @return nothing, it updates the story state based on the clicked choice.
 */
static

/**
 * handles mouse clicks on the story choice buttons.
 *
 * @param button mouse button that was clicked.
 * @param state tells if the button was pressed or released.
 * @param x mouse x position in window coordinates.
 * @param y mouse y position in window coordinates.
 * @return nothing, it updates the story state based on the clicked choice.
 */
    void mouse(int button, int state, int x, int y)
{
    if (button == GLUT_LEFT_BUTTON && state == GLUT_UP)
    {
        float mx = (2.0f * x / win_width) - 1.0f;
        float my = 1.0f - (2.0f * y / win_hight);

        if (sceneState == 2)
        {
            // Bad button
            if (mx >= badBtnX1 && mx <= badBtnX2 &&
                my >= btnY1 && my <= btnY2)
            {
                score--;

                if (scene == 1)
                    grandmaHelped = false;
                else if (scene == 2)
                    kidHelped = false;
                else if (scene == 3)
                    catSatDown = false;

                sceneState = 3;
            }

            // Good button
            else if (mx >= goodBtnX1 && mx <= goodBtnX2 &&
                my >= btnY1 && my <= btnY2)
            {
                score++;

                if (scene == 1)
                    grandmaHelped = true;
                else if (scene == 2)
                    kidHelped = true;
                else if (scene == 3)
                    catSatDown = true;

                sceneState = 3;
            }

            glutPostRedisplay();
        }
    }
}

