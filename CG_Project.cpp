/**
 * handles keyboard input for moving through the story and quitting.
 *
 * @param keyPressed the keyboard key pressed by the user.
 * @param x mouse x position when the key was pressed.
 * @param y mouse y position when the key was pressed.
 * @return nothing, it changes scene states or exits the program.
 */
static

/**
 * handles keyboard input for moving through the story and quitting.
 *
 * @param keyPressed the keyboard key pressed by the user.
 * @param x mouse x position when the key was pressed.
 * @param y mouse y position when the key was pressed.
 * @return nothing, it changes scene states or exits the program.
 */
    void key(unsigned char keyPressed, int x, int y)
{
    switch (keyPressed) {

    case 'n':
    case 'N':
        if (sceneState == 1)
            sceneState = 2; // show buttons after dialogue
        break;

    case 'q':
    case 'Q':
        exit(0);
        break;

    default:
        fprintf(stderr, "\nKeyboard commands:\n\n"
            "n/N         - Continue dialogue\n"
            "q/Q  - Quit\n");
        break;
    }

    glutPostRedisplay();
}

