/**
 * waits for a number of seconds using the real elapsed time from GLUT.
 *
 * @param seconds number of seconds to wait before returning true.
 * @return true if the waiting time is finished, false if it is still waiting.
 *
 * source: https://www.opengl.org/resources/libraries/glut/spec3/node70.html
 * source: https://stackoverflow.com/questions/5357119/constant-game-speed-independent-of-variable-fps-in-opengl-with-glut
 */
bool waitForSeconds(float seconds)
{
    if (!waitStarted)
    {
        waitStartTime = glutGet(GLUT_ELAPSED_TIME);
        waitStarted = true;
    }

    int currentTime = glutGet(GLUT_ELAPSED_TIME);
    int waitTime = seconds * 1000;

    if (currentTime - waitStartTime >= waitTime)
    {
        waitStarted = false;
        return true;
    }

    return false;
}

/**
 * draws a filled circle using the triangle fan method.
 * source: https://gist.github.com/linusthe3rd/803118
 *
 * @param x center x position of the circle.
 * @param y center y position of the circle.
 * @param radius circle radius.
 * @return nothing, it only sends vertices to OpenGL.
 */
void drawFilledCircle(GLfloat x, GLfloat y, GLfloat radius) {
    int i;
    int triangleAmount = 20; // number of triangles used to draw circle

    // GLfloat radius = 0.8f; // radius
    GLfloat twicePi = 2.0f * PI;

    glBegin(GL_TRIANGLE_FAN);
    glVertex2f(x, y); // center point
    for (i = 0; i <= triangleAmount; i++) {
        glVertex2f(
            x + (radius * cos(i * twicePi / triangleAmount)),
            y + (radius * sin(i * twicePi / triangleAmount))
        );
    }
    glEnd();
}

/**
 * draws an outlined circle using the line loop method.
 * source: https://gist.github.com/linusthe3rd/803118
 *
 * @param x center x position of the circle.
 * @param y center y position of the circle.
 * @param radius circle radius.
 * @return nothing, it only draws the circle outline.
 */
void drawHollowCircle(GLfloat x, GLfloat y, GLfloat radius) {
    int i;
    int lineAmount = 100; // number of lines used to draw circle

    // GLfloat radius = 0.8f; // radius
    GLfloat twicePi = 2.0f * PI;

    glBegin(GL_LINE_LOOP);
    for (i = 0; i <= lineAmount; i++) {
        glVertex2f(
            x + (radius * cos(i * twicePi / lineAmount)),
            y + (radius * sin(i * twicePi / lineAmount))
        );
    }
    glEnd();
}

/**
 * loads a bmp image file and creates an OpenGL texture from it.
 * source: http://stackoverflow.com/questions/12518111/how-to-load-a-bmp-on-glut-to-use-it-as-a-texture
 *
 * @param filename path of the bmp file that will be loaded.
 * @param width image width in pixels.
 * @param height image height in pixels.
 * @return the texture id generated by OpenGL.
 */
GLuint LoadTexture(const char* filename, int width, int height)
{
    GLuint texture;
    unsigned char* data;

    // reads the raw image file
    FILE* file;
    // file = fopen(filename, "rb");
    fopen_s(&file, filename, "rb");

    if (file == NULL)
    {
        cout << "Unable to open the image file" << endl << "Program will exit :(" << endl;
        exit(0);
        return 0;
    }

    data = (unsigned char*)malloc(width * height * 3);
    fread(data, width * height * 3, 1, file);

    fclose(file);

    // reorder the image colors to RGB not BGR
    for (int i = 0; i < width * height; ++i)
    {
        int index = i * 3;
        unsigned char B, R;
        B = data[index];
        R = data[index + 2];

        data[index] = R;
        data[index + 2] = B;

    }

    // texture setup
    glGenTextures(1, &texture); // generate the texture with the loaded data
    glBindTexture(GL_TEXTURE_2D, texture); // bind the texture to it's array

    glTexEnvf(GL_TEXTURE_ENV, GL_TEXTURE_ENV_MODE, GL_REPLACE); // set texture environment parameters GL_REPLACE GL_MODULATE

    glTexParameterf(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
    glTexParameterf(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
    glTexParameterf(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_REPEAT);
    glTexParameterf(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_REPEAT);

    glTexImage2D(GL_TEXTURE_2D, 0, GL_RGB, width, height, 0, GL_RGB, GL_UNSIGNED_BYTE, data);

    free(data); // free the texture array

    if (glGetError() != GL_NO_ERROR)
        printf("GLError in genTexture()\n");

    return texture; // return whether it was successfull
}

/**
 * draws a simple filled rectangle using four corner points.
 *
 * @param x1 left x coordinate.
 * @param y1 bottom y coordinate.
 * @param x2 right x coordinate.
 * @param y2 top y coordinate.
 * @return nothing, it draws directly on the screen.
 */
void drawQuad(float x1, float y1, float x2, float y2)
{
    glBegin(GL_QUADS);
    glVertex2f(x1, y1);
    glVertex2f(x2, y1);
    glVertex2f(x2, y2);
    glVertex2f(x1, y2);
    glEnd();
}

