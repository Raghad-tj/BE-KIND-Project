/**
 * draws a rectangle and covers it with the selected texture.
 *
 * @param x1 left x coordinate.
 * @param y1 bottom y coordinate.
 * @param x2 right x coordinate.
 * @param y2 top y coordinate.
 * @param texture OpenGL texture id used on the rectangle.
 * @return nothing, it only draws the textured shape.
 */
void drawTexturedRect(float x1, float y1, float x2, float y2, GLuint texture)
{
    glEnable(GL_TEXTURE_2D);
    glBindTexture(GL_TEXTURE_2D, texture);

    glColor3f(1.0f, 1.0f, 1.0f);

    glBegin(GL_QUADS);
    glTexCoord2f(0.0f, 0.0f); glVertex2f(x1, y1);
    glTexCoord2f(1.0f, 0.0f); glVertex2f(x2, y1);
    glTexCoord2f(1.0f, 1.0f); glVertex2f(x2, y2);
    glTexCoord2f(0.0f, 1.0f); glVertex2f(x1, y2);
    glEnd();

    glBindTexture(GL_TEXTURE_2D, 0);
    glDisable(GL_TEXTURE_2D);

    glColor3f(1.0f, 1.0f, 1.0f);
}

