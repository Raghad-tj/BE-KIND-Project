/**
 * writes text on the screen using GLUT bitmap characters.
 * source: text in OpenGL by Prof. David Bernstein.
 * https://users.cs.jmu.edu/bernstdh/web/common/lectures/summary_opengl-text.php
 *
 * @param x x position where the text starts.
 * @param y y position where the text starts.
 * @param text the message that will be displayed.
 * @return nothing, it prints the text on the OpenGL window.
 */
void drawText(float x, float y, const char* text)
{
    glRasterPos2f(x, y);

    for (int i = 0; text[i] != '\0'; i++) {
        glutBitmapCharacter(GLUT_BITMAP_TIMES_ROMAN_24, text[i]);
    }
}

/**
 * draws one clickable button with a label inside it.
 *
 * @param x1 left x coordinate of the button.
 * @param y1 bottom y coordinate of the button.
 * @param x2 right x coordinate of the button.
 * @param y2 top y coordinate of the button.
 * @param r red color value for the button.
 * @param g green color value for the button.
 * @param b blue color value for the button.
 * @param label text shown inside the button.
 * @return nothing, it draws the button and its label.
 */
void drawButton(float x1, float y1, float x2, float y2,
    float r, float g, float b, const char* label)
{
    drawRectWithOutline(x1, y1, x2, y2, r, g, b);

    glColor3f(1.0f, 1.0f, 1.0f);

    int len = 0;
    while (label[len] != '\0')
        len++;

    float textX = (x1 + x2) / 2.0f - (len * 0.022f);
    float textY = (y1 + y2) / 2.0f - 0.025f;

    drawText(textX, textY, label);
}

